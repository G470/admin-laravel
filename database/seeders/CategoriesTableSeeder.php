    /**
     * Recursively create categories with their children
     */
    private function createCategoriesRecursively($categories, $parentId = null)
    {
        foreach ($categories as $categoryData) {
            $children = $categoryData['children'] ?? [];
            unset($categoryData['children']);

            $categoryData['parent_id'] = $parentId;

            // Check for existing category with same slug
            $category = Category::where('slug', $categoryData['slug'])->first();
            if (!$category) {
                $category = Category::create($categoryData);
            }

            if (!empty($children)) {
                $this->createCategoriesRecursively($children, $category->id);
            }
        }
    }
